// ─────────────────────────────────────────────────────────────────────────────
async function run() {
  console.log('🇨🇳 Забони хитоӣ A1 — аз нол оғоз...\n');

  // 1. Забони хитоиро месозем
  console.log('⏳ Забони хитоиро месозем...');
  const langRes = await api('POST', '/api/admin/languages', {
    code: 'zh',
    name: 'Хитоӣ',
    nativeName: '中文',
    flag: '🇨🇳',
    direction: 'ltr',
    ttsLocale: 'zh-CN',
    isActive: true,
  });
  console.log('Язык:', JSON.stringify(langRes));

  const langId = langRes.language?.id;
  if (!langId) {
    console.error('❌ Забон сохта нашуд!');
    process.exit(1);
  }
  console.log(`✅ Забони хитоӣ ID: ${langId}\n`);

  // 2. Курсро месозем
  console.log('⏳ Курси A1 месозем...');
  const courseRes = await api('POST', '/api/admin/courses', {
    targetLanguageId: langId,
    nativeLanguageId: NATIVE_ID,
    level: 'A1',
    title: 'Забони хитоӣ — A1',
    description: 'Забони хитоиро аз нол омӯзед: пиниин, оҳангҳо, алифбо ва гуфтугӯи ҳаррӯза',
    emoji: '🇨🇳',
    color: '#DC2626',
    order: 0,
    isActive: true,
  });
  console.log('Курс:', JSON.stringify(courseRes));

  const courseId = courseRes.course?.id;
  if (!courseId) {
    console.error('❌ Курс сохта нашуд!');
    process.exit(1);
  }
  console.log(`✅ Курси хитоӣ A1 ID: ${courseId}\n`);

  // 3. Модулҳо, дарсҳо ва калимаҳо
  let totalModules = 0, totalLessons = 0, totalWords = 0;

  for (let mi = 0; mi < MODULES.length; mi++) {
    const m = MODULES[mi];
    console.log(`\n📚 Модул ${mi + 1}/10: ${m.titleTranslated}`);

    const modRes = await api('POST', '/api/admin/modules', {
      courseId,
      title: m.title,
      titleTranslated: m.titleTranslated,
      emoji: m.emoji,
      color: m.color,
      order: mi,
      isPremium: mi >= 5,
      isActive: true,
    });

    if (!modRes.module) {
      console.error('   ❌ Модул хато:', JSON.stringify(modRes));
      continue;
    }
    const moduleId = modRes.module.id;
    totalModules++;
    console.log(`   ✅ ID: ${moduleId}`);

    for (let li = 0; li < m.lessons.length; li++) {
      const l = m.lessons[li];
      const lessonRes = await api('POST', '/api/admin/lessons', {
        moduleId,
        title: l.title,
        titleTranslated: l.titleTranslated,
        type: 'vocab',
        emoji: l.emoji,
        cefrLevel: 'A1',
        skillType: 'vocab',
        xpReward: 60,
        duration: 5,
        order: li,
        isPremium: mi >= 5,
        isActive: true,
      });

      if (!lessonRes.lesson) {
        console.error(`   ❌ Дарс хато: ${JSON.stringify(lessonRes)}`);
        continue;
      }
      const lessonId = lessonRes.lesson.id;
      totalLessons++;

      const wordsPayload = l.words.map((w, wi) => ({
        word: w.w,
        translation: w.t,
        emoji: w.e,
        ipa: w.ipa,
        example: w.ex,
        exampleTrans: w.exT,
        difficulty: 1,
        order: wi,
      }));

      const wordsRes = await api('POST', '/api/admin/words/bulk', {
        lessonId,
        words: wordsPayload,
      });
      const wCount = wordsRes.count ?? l.words.length;
      totalWords += wCount;
      console.log(`   📖 "${l.titleTranslated}" — ${wCount} калима`);
    }
  }

  console.log(`\n${'─'.repeat(50)}`);
  console.log(`✅ Тамом шуд!`);
  console.log(`   Забон:    Хитоӣ (zh) 🇨🇳`);
  console.log(`   Модулҳо:  ${totalModules}`);
  console.log(`   Дарсҳо:   ${totalLessons}`);
  console.log(`   Калимаҳо: ${totalWords}`);
  console.log(`\n🔗 https://admin.ramz.tj/admin/courses/${courseId}`);
}

run().catch(console.error);
