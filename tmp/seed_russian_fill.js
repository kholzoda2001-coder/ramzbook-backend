async function run() {
  console.log('🇷🇺 Пур кардани ' + L.length + ' дарси холии курси русӣ A1\n');
  let okL = 0, totalW = 0;
  for (const lesson of L) {
    const r = await api('POST', '/api/admin/words/bulk', {
      lessonId: lesson.id, mode: 'replace',
      words: lesson.words.map((x, i) => ({
        word: x.w, translation: x.t, emoji: x.e, ipa: x.ipa || undefined,
        example: x.ex, exampleTrans: x.exT, difficulty: 1, order: i,
      })),
    });
    const n = r.count ?? (r.error ? 0 : lesson.words.length);
    if (r.error) { console.error('  ❌ ' + lesson.name + ': ' + JSON.stringify(r)); continue; }
    okL++; totalW += n;
    console.log('  ✅ ' + lesson.name.padEnd(26) + ' — ' + n + ' калима');
  }
  console.log('\n✅ Тамом! ' + okL + '/' + L.length + ' дарс пур шуд, ҳамагӣ ' + totalW + ' калимаи нав.');
  console.log('🔗 https://admin.ramz.tj/admin/courses/cmq95o7ic0001qsy5l76202bw');
}
run().catch(console.error);
