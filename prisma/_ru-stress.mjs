const samples = [];
for (const w of words) {
  const v = build(w.word);
  if (!v) { skip++; continue; }
  if (samples.length < 14) samples.push(`  ${w.word}  →  ${v}`);
  if (dry) { set++; continue; }
  const res = await fetch(`${BASE}/api/admin/words/${w.id}`, {
    method: 'PUT', headers: H, body: JSON.stringify({ ipaTajik: v }),
  });
  if (res.ok) set++; else { fail++; if (fail < 4) console.log(`  ✗ ${w.word}: ${res.status}`); }
}
