// Калимаҳое ки ТАВРИ ДИГАР хонда мешаванд — ишораи қавсдор мегиранд.
const IRREG = {
  'здравствуйте': 'здра́ствуйте — «в» хонда намешавад',
  'что': 'што',
  'сегодня': 'сево́дня',
  'солнце': 'со́нце — «л» хонда намешавад',
  'его': 'ево́',
  'него': 'нево́',
  'ничего': 'ничево́',
  'поздно': 'по́зно — «д» хонда намешавад',
  'пожалуйста': 'дар гуфтугӯ: пожа́лста',
};

const cap = (s) => s.charAt(0).toUpperCase() + s.slice(1);

/** Шакли задагузошта + ишораи қавсдор барои як сабт. */
function build(word) {
  const letters = word.replace(/[^А-Яа-яЁё]/g, '');
  if (letters.length <= 1) return '';           // ҳарфи алифбо — чизе намехоҳад
  let changed = false;
  const hints = [];
  const out = word.replace(/[А-Яа-яЁё]+/g, (tok) => {
    const low = tok.toLowerCase();
    if (IRREG[low]) hints.push(IRREG[low]);
    const st = S[low];
    if (!st) return tok;
    changed = true;
    return tok[0] === tok[0].toUpperCase() ? cap(st) : st.toLowerCase() === st ? st : st;
  });
  if (!changed && !hints.length) return '';      // якҳиҷогӣ/оддӣ — холӣ мемонад
  return hints.length ? `${out}  [${hints.join('; ')}]` : out;
}

