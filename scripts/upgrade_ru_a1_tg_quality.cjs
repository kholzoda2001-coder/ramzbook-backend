function partOfSpeech(word) {
  const w = word.toLowerCase();
  if (/\s/.test(w) || /[?!.]/.test(w)) return 'phrase';
  if (/^(я|ты|он|она|мы|вы|они|мой|моя|моё|твой|ваш|наш)$/.test(w)) return 'pronoun';
  if (/^(в|на|у|около|перед|за|между|слева|справа|рядом|напротив)$/.test(w)) return 'preposition';
  if (/^(и|а|но|потому что)$/.test(w)) return 'conjunction';
  if (/^(один|два|три|четыре|пять|двадцать|тридцать|сорок|сто|тысяча)$/.test(w)) return 'numeral';
  if (/(ть|ться|ет|ит|ю|ешь|ать|ить)$/.test(w)) return 'verb';
  if (/(ый|ий|ая|яя|ое|ее|ой|о)$/.test(w)) return 'adjective';
  return 'noun';
}

function phonetic(word) {
  return `/${word.toLowerCase().replace(/[?!.,]/g, '').replace(/\s+/g, ' ')}/`;
}

function exampleFor(word, translation, pos) {
  if (pos === 'verb') return ['Я могу ' + word + '.', 'Ман метавонам ' + translation + '.'];
  if (pos === 'adjective') return ['Это ' + word + '.', 'Ин ' + translation + ' аст.'];
  if (pos === 'phrase') return [word, translation];
  return ['Это ' + word + '.', 'Ин ' + translation + ' аст.'];
}

