async function audit(courseId) {
  const c = await prisma.course.findUnique({ where: { id: courseId }, include: { modules: { include: { lessons: { include: { words: true } } } }, grammarTopics: { include: { exercises: true } }, phraseCollections: { include: { phrases: true } }, dialogues: { include: { lines: true } }, comprehensions: { include: { questions: true } } } });
  const lessons = c.modules.flatMap((m) => m.lessons);
  const words = lessons.flatMap((l) => l.words);
  const bySkill = {};
  for (const l of lessons) bySkill[l.skillType] = (bySkill[l.skillType] || 0) + 1;
  const grammarTypes = {};
  for (const t of c.grammarTopics) for (const e of t.exercises) grammarTypes[e.type] = (grammarTypes[e.type] || 0) + 1;
  const emptyCriticalLessons = lessons.filter((l) => ['vocab', 'speaking', 'writing', 'review'].includes(l.skillType) && !l.grammarTopicId && !l.phraseCollectionId && !l.dialogueId && !l.comprehensionId && l.words.length === 0).map((l) => l.title);
  return {
    modules: c.modules.length,
    lessons: lessons.length,
    words: words.length,
    wordsMissingPos: words.filter((w) => !w.partOfSpeech).length,
    wordsMissingIpa: words.filter((w) => !w.ipa).length,
    wordsMissingExample: words.filter((w) => !w.example || !w.exampleTrans).length,
    grammarTopics: c.grammarTopics.length,
    grammarExercises: c.grammarTopics.reduce((s, t) => s + t.exercises.length, 0),
    grammarTypes,
    phraseCollections: c.phraseCollections.length,
    phrases: c.phraseCollections.reduce((s, x) => s + x.phrases.length, 0),
    dialogues: c.dialogues.length,
    dialogueLines: c.dialogues.reduce((s, x) => s + x.lines.length, 0),
    comprehensions: c.comprehensions.length,
    comprehensionQuestions: c.comprehensions.reduce((s, x) => s + x.questions.length, 0),
    bySkill,
    emptyCriticalLessons,
  };
}

